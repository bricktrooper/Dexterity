/* This header contains string substitutions
 * for ANSI terminal colours. */

#ifndef COLOURS_H
#define COLOURS_H

#define ORANGE    "\033[30m"
#define RED       "\033[31m"
#define GREEN     "\033[32m"
#define YELLOW    "\033[33m"
#define BLUE      "\033[34m"
#define PINK      "\033[35m"
#define CYAN      "\033[36m"
#define PURPLE    "\033[37m"
#define WHITE     "\033[0m"

#endif /* COLOURS_H */
